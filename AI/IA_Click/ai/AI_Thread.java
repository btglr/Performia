	/*================================<DEFINE>================================*/
package ai;

	/*================================<GENERAL>================================*/
import org.json.JSONObject;

	/*================================<PERSONEL>================================*/

	/*================================<DECLARATION>================================*/
public class AI_Thread extends Thread{
	
/*
 * //===========================================================================================//
 * //										THREAD												//
 * //===========================================================================================//
 * */
	/*================================<DECLARATION>================================*/
	Thread t;
	int id;
	TCPClient tcpClient;
	JSONObject data;
	
	/*================================<CONSTRUCTEUR>================================*/
	public AI_Thread(int i,TCPClient tcpClient)
	{
		this.data = new JSONObject();
		data.put("id_utilisateur", tcpClient.getUserId());
		data.put("case", i);
		this.start();
		this.tcpClient = tcpClient;
	}

	/*================================<FONCTION>================================*/
	public void run(){
		try {
			// Attente entre 100 et 200 ms 
			sleep((long) (100 + (Math.random() * 100)));
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
		tcpClient.sendTurn(data);
	}
}








